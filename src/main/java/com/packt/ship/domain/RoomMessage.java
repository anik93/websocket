package com.packt.ship.domain;

public class RoomMessage {
	
	private int id_r;
	private int row;
	private int columm;
	
	public int getId_r() {
		return id_r;
	}
	public void setId_r(int id_r) {
		this.id_r = id_r;
	}
	public int getRow() {
		return row;
	}
	public void setRow(int row) {
		this.row = row;
	}
	public int getColumm() {
		return columm;
	}
	public void setColumm(int columm) {
		this.columm = columm;
	}
	
}
